package JavaBasics;
public class RelationalOperatorsDemo {
   
public static void main(String[] args) {
   
   int number1 = 20; 

   int number2 = 20; 

   // can be <, <=, >, >=, ==, !=
   // == must be equal 
   // != must not be equal
    System.out.println(number1 <= number2);

 }
}