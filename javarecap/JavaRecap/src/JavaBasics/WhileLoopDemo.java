package JavaBasics;
public class WhileLoopDemo {
    
public static void main(String[] args) {
    
    int number = 1;

    // another way to repeat a method with repition 
    // while nubmer is not <= 5, keep repeating this task 
    // Hello Java is repeated 5 times 

    while (number <= 5) {

        System.out.println("Hello Java");

        number ++;

    }

}

}
