package JavaBasics;
public class ForLoopDemo {
    
    public static void main (String [] args) {

        // instead of repeating the line of code you can use a for loop 
        // this for loop prints hello world 6 times
        // int i = 0 is the start of the for loop 
        // i <=5 is that the loop will run until it gets to 5 (0 counts, so this loop runs 6 times)
        // i++ is the counter 

        for (int i = 0; i <= 5; i++) {

            System.out.println("Hello World");

        }

    }

}
