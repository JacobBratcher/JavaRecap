package JavaBasics;
public class LogicalOperatorsDemo {
    
    public static void main(String[] args) {

        int number1 =30;

        int number2 = 20;

        int number3 = 10;

        // && is used to put together true/false statements 
        System.out.println(number1 > number2 && number1 > number3);

        // || is used as either or with true/false statements
          System.out.println(number1 > number2 || number1 > number3);

    }

}
